export const randomizeData = (coordinatedData) => {
  return coordinatedData.map((r) => {
    if (r.value == 0 || r.value == 2) {
      return r;
    }
    return {
      ...r,
      value: Math.random() > 0.99 ? 2 : 1
    };
  });
};
