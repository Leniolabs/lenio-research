export const getCoordinates = (rows) => {
  return rows.reduce((acc, curr, rowIdx) => {
    const t = curr
      .map((elem, idx) => {
        return {
          x: idx,
          y: rowIdx,
          value: elem
        };
      })
      .filter((elem) => elem.value !== 0);
    acc.push(t);
    return acc.flat();
  }, []);
};

